package com.shilen.app.workbench.model.wo;

public class SearchResults {
	
	private String status;
    private String caliber;
    private String steeltype;
    private String rifling;
    private String woid;
    private String created;
    
    
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCaliber() {
		return caliber;
	}
	public void setCaliber(String caliber) {
		this.caliber = caliber;
	}
	public String getRifling() {
		return rifling;
	}
	public void setRifling(String rifling) {
		this.rifling = rifling;
	}
	public String getWoid() {
		return woid;
	}
	public void setWoid(String woid) {
		this.woid = woid;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getSteeltype() {
		return steeltype;
	}
	public void setSteeltype(String steeltype) {
		this.steeltype = steeltype;
	}
      

}
